pub fn header(colors: &ColorScheme, comment: &str) -> String {
    format!(
        "{comment} Generated by apply-theme — do not edit\n{comment} Source: {}-{} | Variant: {}\n",
        colors.family, colors.variant, colors.variant
    )
}

pub fn apply_target(
    registry: &TargetRegistry,
    name: &str,
    colors: &ColorScheme,
    state: &ThemeState,
    runtime: bool,
) -> bool {
    let Some(target) = registry.get(name) else {
        eprintln!("error: unknown target '{name}'");
        return false;
    };

    let result = (|| -> crate::Result<()> {
        let content = target.generate(colors, state)?;
        assemble(target, content, colors)?;
        persist(target, colors, state)?;
        if runtime {
            reload(target, colors, state);
        }
        Ok(())
    })();

    match result {
        Ok(()) => {
            println!("  {name}: ok");
            true
        }
        Err(error) => {
            eprintln!("  {name}: FAILED — {error}");
            false
        }
    }
}

pub fn apply_targets<I, S>(
    registry: &TargetRegistry,
    names: I,
    colors: &ColorScheme,
    state: &ThemeState,
    runtime: bool,
) -> bool
where
    I: IntoIterator<Item = S>,
    S: AsRef<str>,
{
    let mut available = Vec::new();
    let mut skipped = Vec::new();

    for name in names {
        let name = name.as_ref();
        if registry.get(name).is_some() {
            available.push(name.to_owned());
        } else {
            skipped.push(name.to_owned());
        }
    }

    if !skipped.is_empty() {
        skipped.sort();
        println!("  (skipping unregistered: {})", skipped.join(", "));
    }

    let mut ok = true;
    for name in sorted_targets(registry, available) {
        ok = apply_target(registry, &name, colors, state, runtime) && ok;
    }
    ok
}

pub fn apply_targets_quiet<I, S>(
    registry: &TargetRegistry,
    names: I,
    colors: &ColorScheme,
    state: &ThemeState,
    runtime: bool,
) -> crate::Result<()>
where
    I: IntoIterator<Item = S>,
    S: AsRef<str>,
{
    let mut available = Vec::new();

    for name in names {
        let name = name.as_ref();
        if registry.get(name).is_some() {
            available.push(name.to_owned());
        }
    }

    let mut failures = Vec::new();
    for name in sorted_targets(registry, available) {
        if let Err(error) = apply_target_quiet(registry, &name, colors, state, runtime) {
            failures.push(format!("{name}: {error}"));
        }
    }

    if failures.is_empty() {
        Ok(())
    } else {
        Err(io::Error::other(failures.join("; ")).into())
    }
}

pub fn apply_all(
    registry: &TargetRegistry,
    colors: &ColorScheme,
    state: &ThemeState,
    runtime: bool,
    sync_safe: bool,
) -> bool {
    let names = all_target_names(registry, sync_safe);
    apply_targets(registry, names, colors, state, runtime)
}

pub fn color_targets() -> BTreeSet<String> {
    named_targets(COLOR_TARGETS)
}

pub fn font_targets() -> BTreeSet<String> {
    named_targets(
        SYSTEM_FONT_TARGETS
            .iter()
            .chain(MONO_FONT_TARGETS.iter())
            .copied(),
    )
}

pub fn targets_for_key(state_key: &str, state: Option<&ThemeState>) -> BTreeSet<String> {
    let mut targets = match state_key {
        "color_scheme" => named_targets(
            COLOR_TARGETS
                .into_iter()
                .chain(std::iter::once("wallpaper")),
        ),
        _ => named_targets(dependency_targets(state_key).iter().copied()),
    };

    if state_key == "color_scheme" && matches!(state, Some(state) if !state.filter_wallpaper) {
        targets.remove("wallpaper");
    }

    targets
}

fn named_targets<I>(names: I) -> BTreeSet<String>
where
    I: IntoIterator<Item = &'static str>,
{
    names.into_iter().map(str::to_owned).collect()
}

