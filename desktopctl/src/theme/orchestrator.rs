fn apply_target_quiet(
    registry: &TargetRegistry,
    name: &str,
    colors: &ColorScheme,
    state: &ThemeState,
    runtime: bool,
) -> crate::Result<()> {
    let target = registry.get(name).ok_or_else(|| {
        io::Error::new(io::ErrorKind::NotFound, format!("unknown target '{name}'"))
    })?;

    let content = target.generate(colors, state)?;
    assemble(target, content, colors)?;
    persist(target, colors, state)?;
    if runtime {
        reload_quiet(target, colors, state);
    }
    Ok(())
}

fn assemble(
    target: &dyn Target,
    content: GeneratedContent,
    colors: &ColorScheme,
) -> crate::Result<()> {
    let metadata = target.metadata();
    match metadata.assembly {
        Assembly::Command => run_command_batch(content),
        Assembly::Import | Assembly::Standalone => {
            let rendered = match content {
                GeneratedContent::Text(content) => {
                    let header = metadata
                        .comment
                        .map(|comment| format!("{}\n", header(colors, comment)))
                        .unwrap_or_default();
                    format!("{header}{content}")
                }
                GeneratedContent::Commands(_) => {
                    return Err(io::Error::new(
                        io::ErrorKind::InvalidInput,
                        format!(
                            "Target '{}' returned commands for a file-writing assembly",
                            metadata.name
                        ),
                    )
                    .into());
                }
            };
            let output_path = output_path(metadata)?;
            write_output(&output_path, &rendered)?;
            write_extra_outputs(metadata, &rendered)?;
            Ok(())
        }
        Assembly::Concat => assemble_concat(metadata, content, colors),
    }
}

fn assemble_concat(
    metadata: &crate::theme::targets::TargetMetadata,
    content: GeneratedContent,
    colors: &ColorScheme,
) -> crate::Result<()> {
    let output_path = output_path(metadata)?;
    let base_path = base_path(metadata)?;

    if !base_path.exists() {
        return Err(io::Error::new(
            io::ErrorKind::NotFound,
            format!(
                "base file missing for target '{}': {}",
                metadata.name,
                base_path.display()
            ),
        )
        .into());
    }

    let rendered = match content {
        GeneratedContent::Text(content) => {
            if output_path
                .extension()
                .is_some_and(|extension| extension == "json")
            {
                concat_json(&base_path, &content)?
            } else {
                let base = fs::read_to_string(&base_path)?;
                let header = metadata
                    .comment
                    .map(|comment| format!("{}\n", header(colors, comment)))
                    .unwrap_or_default();
                format!("{}\n{header}{content}", trim_trailing_newlines(&base))
            }
        }
        GeneratedContent::Commands(_) => {
            return Err(io::Error::new(
                io::ErrorKind::InvalidInput,
                format!(
                    "Target '{}' returned commands for concat assembly",
                    metadata.name
                ),
            )
            .into());
        }
    };

    write_output(&output_path, &rendered)?;
    write_extra_outputs(metadata, &rendered)?;
    Ok(())
}

fn concat_json(base_path: &Path, generated: &str) -> crate::Result<String> {
    let base_value: Value =
        serde_json::from_str(&fs::read_to_string(base_path)?).map_err(|error| {
            io::Error::new(
                io::ErrorKind::InvalidData,
                format!("Invalid JSON in {}: {error}", base_path.display()),
            )
        })?;
    let generated_value: Value = serde_json::from_str(generated).map_err(|error| {
        io::Error::new(
            io::ErrorKind::InvalidData,
            format!("Invalid generated JSON for concat target: {error}"),
        )
    })?;

    let Value::Object(base_map) = base_value else {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            format!("{}: expected top-level JSON object", base_path.display()),
        )
        .into());
    };
    let Value::Object(generated_map) = generated_value else {
        return Err(io::Error::new(
            io::ErrorKind::InvalidData,
            "Generated concat JSON must be a top-level object",
        )
        .into());
    };

    let merged = merge_json_objects(base_map, generated_map);
    Ok(format!(
        "{}\n",
        json::format_pretty_value(&Value::Object(merged))
    ))
}

fn merge_json_objects(
    base: Map<String, Value>,
    generated: Map<String, Value>,
) -> Map<String, Value> {
    let mut merged = base;

    for (key, value) in generated {
        match (merged.get_mut(&key), value) {
            (Some(Value::Object(base_child)), Value::Object(generated_child)) => {
                let mut merged_child = base_child.clone();
                for (nested_key, nested_value) in generated_child {
                    merged_child.insert(nested_key, nested_value);
                }
                *base_child = merged_child;
            }
            (_, value) => {
                merged.insert(key, value);
            }
        }
    }

    merged
}

fn persist(target: &dyn Target, colors: &ColorScheme, state: &ThemeState) -> crate::Result<()> {
    target.persist(colors, state)
}

fn reload(target: &dyn Target, colors: &ColorScheme, state: &ThemeState) {
    let metadata = target.metadata();

    if let Some(command) = metadata.reload_cmd
        && let Err(error) = run_command(command)
    {
        eprintln!("  reload warning ({}): {}", metadata.name, error);
    }

    if let Err(error) = target.on_apply(colors, state) {
        eprintln!("  reload warning ({}): {}", metadata.name, error);
    }
}

fn reload_quiet(target: &dyn Target, colors: &ColorScheme, state: &ThemeState) {
    let metadata = target.metadata();

    if let Some(command) = metadata.reload_cmd {
        let _ = run_command(command);
    }

    let _ = target.on_apply(colors, state);
}

fn run_command_batch(content: GeneratedContent) -> crate::Result<()> {
    let GeneratedContent::Commands(commands) = content else {
        return Err(io::Error::new(
            io::ErrorKind::InvalidInput,
            "Command target returned text instead of commands",
        )
        .into());
    };

    let mut failures = Vec::new();
    for command in commands {
        if let Err(error) = run_owned_command(&command) {
            failures.push(error.to_string());
        }
    }

    if failures.is_empty() {
        Ok(())
    } else {
        Err(io::Error::other(failures.join("; ")).into())
    }
}

fn run_command(command: &[&str]) -> crate::Result<()> {
    theme::run_command(command)
}

fn run_owned_command(command: &[String]) -> crate::Result<()> {
    theme::run_owned_command(command)
}

fn write_output(path: &Path, content: &str) -> crate::Result<()> {
    theme::atomic_write(path, content.as_bytes())
}

fn write_extra_outputs(
    metadata: &crate::theme::targets::TargetMetadata,
    content: &str,
) -> crate::Result<()> {
    for extra in metadata.extra_outputs {
        let path = theme::expand_user_path(extra)?;
        write_output(&path, content)?;
    }
    Ok(())
}

fn output_path(
    metadata: &crate::theme::targets::TargetMetadata,
) -> crate::Result<std::path::PathBuf> {
    let path = metadata.output_path.ok_or_else(|| {
        io::Error::new(
            io::ErrorKind::InvalidInput,
            format!("Target '{}' is missing OUTPUT_PATH", metadata.name),
        )
    })?;
    theme::expand_user_path(path)
}

fn base_path(metadata: &crate::theme::targets::TargetMetadata) -> crate::Result<PathBuf> {
    let path = metadata.base_path.ok_or_else(|| {
        io::Error::new(
            io::ErrorKind::InvalidInput,
            format!("Target '{}' is missing BASE_PATH", metadata.name),
        )
    })?;
    resolve_base_path(path)
}

fn resolve_base_path(path: &str) -> crate::Result<PathBuf> {
    let candidate = Path::new(path);
    if path.starts_with('~') || candidate.is_absolute() {
        return theme::expand_user_path(path);
    }

    Ok(paths::repo_path(candidate)?)
}

fn trim_trailing_newlines(text: &str) -> &str {
    text.trim_end_matches('\n')
}

fn sorted_targets(registry: &TargetRegistry, mut names: Vec<String>) -> Vec<String> {
    names.sort_by(|left, right| {
        let left_key = target_sort_key(registry, left);
        let right_key = target_sort_key(registry, right);
        left_key.cmp(&right_key)
    });
    names
}

fn target_sort_key<'a>(registry: &TargetRegistry, name: &'a str) -> (u8, &'a str) {
    let assembly_rank = registry
        .get(name)
        .map(|target| target.metadata().assembly)
        .map(|assembly| if assembly == Assembly::Command { 1 } else { 0 })
        .unwrap_or(0);

    (assembly_rank, name)
}

fn all_target_names(registry: &TargetRegistry, sync_safe: bool) -> Vec<String> {
    registry
        .iter()
        .filter(|(_, target)| !sync_safe || target.metadata().sync_safe)
        .map(|(name, _)| name.to_owned())
        .collect()
}

