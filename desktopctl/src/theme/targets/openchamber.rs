    primary.insert(
        "hover".to_owned(),
        Value::String(colors.blue_bright.clone()),
    );
    primary.insert("active".to_owned(), Value::String(colors.blue.clone()));
    primary.insert(
        "foreground".to_owned(),
        Value::String(emphasis_foreground.clone()),
    );
    primary.insert(
        "muted".to_owned(),
        Value::String(with_alpha(&colors.accent, "80")),
    );
    primary.insert(
        "emphasis".to_owned(),
        Value::String(colors.blue_bright.clone()),
    );

    let mut surface = Map::new();
    surface.insert("background".to_owned(), Value::String(colors.bg.clone()));
    surface.insert("foreground".to_owned(), Value::String(colors.fg.clone()));
    surface.insert(
        "muted".to_owned(),
        Value::String(with_alpha(&colors.bg_dim, "90")),
    );
    surface.insert(
        "mutedForeground".to_owned(),
        Value::String(colors.fg3.clone()),
    );
    surface.insert(
        "elevated".to_owned(),
        Value::String(with_alpha(&colors.bg1, "90")),
    );
    surface.insert(
        "elevatedForeground".to_owned(),
        Value::String(colors.fg.clone()),
    );
    surface.insert(
        "overlay".to_owned(),
        Value::String(with_alpha(&colors.bg, "80")),
    );
    surface.insert("subtle".to_owned(), Value::String(colors.bg2.clone()));

    let mut interactive = Map::new();
    interactive.insert("border".to_owned(), Value::String(colors.bg3.clone()));
