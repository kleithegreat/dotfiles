    interactive.insert(
        "borderFocus".to_owned(),
        Value::String(colors.accent.clone()),
    );
    interactive.insert(
        "selection".to_owned(),
        Value::String(with_alpha(&colors.accent, "26")),
    );
    interactive.insert(
        "selectionForeground".to_owned(),
        Value::String(colors.fg.clone()),
    );
    interactive.insert("focus".to_owned(), Value::String(colors.accent.clone()));
    interactive.insert(
        "focusRing".to_owned(),
        Value::String(with_alpha(&colors.accent, "50")),
    );
    interactive.insert("cursor".to_owned(), Value::String(colors.fg.clone()));
    interactive.insert(
        "hover".to_owned(),
        Value::String(with_alpha(&colors.fg, "12")),
    );
    interactive.insert(
        "active".to_owned(),
        Value::String(with_alpha(&colors.fg, "18")),
    );

    let mut status = Map::new();
    insert_status_group(
        &mut status,
        "error",
        &colors.red,
        &emphasis_foreground,
        "20",
        "50",
    );
    insert_status_group(
        &mut status,
        "warning",
        &colors.yellow,
        &emphasis_foreground,
        "20",
        "50",
    );
    insert_status_group(
        &mut status,
        "success",
        &colors.green,
        &emphasis_foreground,
        "20",
        "50",
    );
    insert_status_group(
        &mut status,
        "info",
        &colors.blue,
        &emphasis_foreground,
        "20",
        "50",
    );

    let mut pr = Map::new();
    pr.insert("open".to_owned(), Value::String(colors.green.clone()));
    pr.insert("draft".to_owned(), Value::String(colors.fg3.clone()));
    pr.insert("blocked".to_owned(), Value::String(colors.yellow.clone()));
    pr.insert("merged".to_owned(), Value::String(colors.purple.clone()));
    pr.insert("closed".to_owned(), Value::String(colors.red.clone()));

    let mut syntax_base = Map::new();
    syntax_base.insert("background".to_owned(), Value::String(colors.bg1.clone()));
    syntax_base.insert("foreground".to_owned(), Value::String(colors.fg.clone()));
